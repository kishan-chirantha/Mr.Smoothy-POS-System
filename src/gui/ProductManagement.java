    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed

        try {

            String productID = jTextField1.getText();
            String productName = jTextField2.getText();
            String category = String.valueOf(jComboBox1.getSelectedItem());

            if (productName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Product name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (category.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please select Category", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `product` WHERE `name`='" + productName + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "This Product is Already Added", "Message", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.executeIUD("INSERT INTO `product`(`id`,`name`,`categories_id`) VALUES ('" + productID + "','" + productName + "','" + categoryMap.get(category) + "')");

                    JOptionPane.showMessageDialog(this, "Successfully", "Infromation", JOptionPane.INFORMATION_MESSAGE);

                    loadProduct();
                    reset();
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton8ActionPerformed

    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed

        try {

            String productID = jTextField1.getText();
            String productName = jTextField2.getText();
            String category = String.valueOf(jComboBox1.getSelectedItem());

            if (productName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Product name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (category.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please select Category", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `product` WHERE `name`='" + productName + "' AND `categories_id`='" + categoryMap.get(category) + "'");

                if (resultSet.next()) {

                    JOptionPane.showMessageDialog(this, "Please change Details to Update", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    MySQL.executeIUD("UPDATE `product` SET `name`='" + productName + "',`categories_id`='" + categoryMap.get(category) + "' WHERE `id`='" + productID + "'");

                    loadProduct();
                    reset();
                    JOptionPane.showMessageDialog(this, "Product Updated", "Infromation", JOptionPane.INFORMATION_MESSAGE);

                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton9ActionPerformed

