    public void loadCategory() {

        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `categories`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                categoryMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }

            DefaultComboBoxModel modal = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(modal);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void loadProduct() {

        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `product` INNER JOIN `categories` ON `product`.`categories_id`=`categories`.`id`");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("name"));
                vector.add(resultSet.getString("categories.name"));
                dtm.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();

        }

    }
    
