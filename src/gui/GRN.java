    private void jFormattedTextField5KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFormattedTextField5KeyReleased

        String total = jFormattedTextField4.getText();
        String payment = jFormattedTextField5.getText();

        if (payment.isEmpty()) {
            payment = "0";
        } else if (!payment.matches("^(0|[1-9]\\d*)?(\\.\\d+)?(?<=\\d)$")) {

            jFormattedTextField6.setText("INVALID");
            jFormattedTextField6.setForeground(Color.RED);

        } else {

            double balance = Double.parseDouble(payment) - Double.parseDouble(total);
            jFormattedTextField6.setText(String.valueOf(balance));
            jFormattedTextField6.setForeground(Color.BLACK);

        }
    }//GEN-LAST:event_jFormattedTextField5KeyReleased

