    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        try {

            String grnNmb = jLabel7.getText();
            String supMobile = jLabel20.getText();
            String date = new SimpleDateFormat("yyyy-MM-dd").format(new Date());
            String paid_amount = jFormattedTextField5.getText();
            String total = jFormattedTextField4.getText();

            if (total.equals("")) {
                JOptionPane.showMessageDialog(this, "Please Add GRN First", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (paid_amount.equals("")) {
                JOptionPane.showMessageDialog(this, "Type Paid Amount", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                ResultSet resultSet3 = MySQL.executeSearch("SELECT * FROM grn WHERE `id`='" + grnNmb + "'");

                if (resultSet3.next()) {
                    JOptionPane.showMessageDialog(this, "This GRN is Already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.executeIUD("INSERT INTO `grn`(`id`,`supplier_mobile`,`date`,`paid_amount`) VALUES ('" + grnNmb + "','" + supMobile + "','" + date + "','" + paid_amount + "')");

                    for (GRNItem grnItem : grnItemMap.values()) {

                        ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `stock` WHERE `product_id` ='" + grnItem.getProductId() + "' AND `price`='" + grnItem.getSellingPrice() + "'");

                        String sid = "";

                        if (resultSet.next()) {

                            sid = resultSet.getString("id");

                            String currentQty = resultSet.getString("qty");
                            String updatedQty = String.valueOf(Double.parseDouble(currentQty) + grnItem.getQty());
                            MySQL.executeIUD("UPDATE `stock` SET `qty` = '" + updatedQty + "' WHERE `id`='" + sid + "'");

                        } else {

                            MySQL.executeIUD("INSERT INTO `stock`(`product_id`,`qty`,`price`) VALUES ('" + grnItem.getProductId() + "','" + grnItem.getQty() + "','" + grnItem.getSellingPrice() + "')");

                            ResultSet resultSet2 = MySQL.executeSearch("SELECT * FROM `stock` WHERE `product_id`='" + grnItem.getProductId() + "' AND `price`='" + grnItem.getSellingPrice() + "' ");

                            if (resultSet2.next()) {
                                sid = resultSet2.getString("id");
                            }
                        }

                        MySQL.executeIUD("INSERT INTO `grn_item`(`qty`,`price`,`grn_id`,`stock_id`) VALUES ('" + grnItem.getQty() + "','" + grnItem.getBuyingPrice() + "','" + grnNmb + "','" + sid + "')");

                        JOptionPane.showMessageDialog(this, "Successfully", "Infromation", JOptionPane.INFORMATION_MESSAGE);

                    }

                }
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton14ActionPerformed

